package DataTypes;

public class decimalDataTypes {
    public static void main(String[] args) {
        float vfloat = 1.5f;       // 32 bit boyutunda ama float olması için sonuna f || F koymak lazım :P
        double vdouble = 3.31;     // 64 bit boyutunda her sayıyı alıyor bu çok büyük


        System.out.println(vfloat);
        System.out.println(vdouble);
    }
}
